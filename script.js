// ==================== COUNTDOWN ====================
function initCountdown() {
  const launchDate = new Date('2026-06-01T00:00:00');
  
  const daysEl = document.getElementById('days');
  const hoursEl = document.getElementById('hours');
  const minutesEl = document.getElementById('minutes');
  const secondsEl = document.getElementById('seconds');
  
  if (!daysEl) return;
  
  function pad(n) { return String(n).padStart(2, '0'); }
  
  function updateFlip(el, newVal) {
    const oldVal = el.textContent;
    if (oldVal !== newVal) {
      el.classList.remove('flip');
      void el.offsetWidth; // reflow
      el.classList.add('flip');
      el.textContent = newVal;
    }
  }
  
  function tick() {
    const now = new Date();
    const diff = launchDate - now;
    
    if (diff <= 0) {
      daysEl.textContent = hoursEl.textContent = minutesEl.textContent = secondsEl.textContent = '00';
      return;
    }
    
    updateFlip(daysEl, pad(Math.floor(diff / 86400000)));
    updateFlip(hoursEl, pad(Math.floor((diff % 86400000) / 3600000)));
    updateFlip(minutesEl, pad(Math.floor((diff % 3600000) / 60000)));
    updateFlip(secondsEl, pad(Math.floor((diff % 60000) / 1000)));
  }
  
  tick();
  setInterval(tick, 1000);
  
  // Animate progress bar when in view
  const progressFill = document.querySelector('.countdown-progress-fill');
  if (progressFill) {
    const observer = new IntersectionObserver((entries) => {
      entries.forEach(entry => {
        if (entry.isIntersecting) {
          setTimeout(() => {
            progressFill.style.width = progressFill.style.width || '22.47%';
          }, 500);
          observer.unobserve(entry.target);
        }
      });
    }, { threshold: 0.3 });
    observer.observe(progressFill);
  }
}

